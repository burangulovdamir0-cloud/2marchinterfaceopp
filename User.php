<?php
class User implements iUser {
    private string $name;
    private int $age;

    public function __construct(string $name, int $age) {
        $this->name = $name;
        $this->age = $age;
    }

    public function getName(): string {
        return $this->name;
    }

    public function getAge(): int {
        return $this->age;
    }
}
$user = new User("Иван", 25);
echo $user->getName(); 
echo $user->getAge();
